#!/bin/bash

# ============================================================================
# OpenClaw 环境清理脚本 v3.0
# 用途：清理 OPENCLAW_HOME 等环境变量，防止污染生产环境
# 作者：墨墨 (Mò)
# 版本：3.0.0 (基于环境清理原则)
# ============================================================================

# 颜色定义
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m' # No Color

# ============================================================================
# 函数：清理环境变量
# ============================================================================
cleanup_env() {
    echo -e "${BLUE}清理环境变量...${NC}"
    
    # 清理 OPENCLAW_HOME
    if [ ! -z "$OPENCLAW_HOME" ]; then
        unset OPENCLAW_HOME
        echo -e "${GREEN}✓ OPENCLAW_HOME 已清理${NC}"
    else
        echo -e "${YELLOW}ℹ OPENCLAW_HOME 未设置${NC}"
    fi
    
    # 清理 HOME（如果被修改过）
    if [ "$HOME" != "/Users/wh1ko" ]; then
        export HOME="/Users/wh1ko"
        echo -e "${GREEN}✓ HOME 已恢复${NC}"
    fi
    
    echo -e "${GREEN}✓ 环境清理完成${NC}"
}

# ============================================================================
# 函数：检查当前环境
# ============================================================================
check_env() {
    echo -e "${BLUE}当前环境状态:${NC}"
    echo "  OPENCLAW_HOME: ${OPENCLAW_HOME:-未设置}"
    echo "  HOME: $HOME"
    echo ""
}

# ============================================================================
# 主流程
# ============================================================================
main() {
    echo -e "${BLUE}╔════════════════════════════════════════════════════════╗${NC}"
    echo -e "${BLUE}║   OpenClaw 环境清理 v3.0                               ║${NC}"
    echo -e "${BLUE}╚════════════════════════════════════════════════════════╝${NC}"
    echo ""
    
    check_env
    cleanup_env
    
    echo ""
    echo -e "${GREEN}✓ 环境清理完成，可以安全执行生产命令${NC}"
}

# 执行
main
